CREATE TABLE pfps (
	id INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	data BLOB NOT NULL
);

CREATE TABLE users (
	username TEXT NOT NULL PRIMARY KEY,
	password BLOB NOT NULL,
	name TEXT NOT NULL,
	pronouns TEXT,
	bio TEXT,
	age INTEGER,
	year INTEGER,
	pfp INTEGER NOT NULL REFERENCES pfps(id) DEFAULT 0
);

CREATE TABLE friends (
    username1 TEXT,
    username2 TEXT
);
